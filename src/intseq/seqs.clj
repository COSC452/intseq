(ns intseq.seqs)

(defn simple []
  "A simple sequence."
  (for [x (range 0 10 1)]
              [x (+ (* x x) (+ 2 x) 2)]))

(defn A037270 []
  "Mathematica: Table[n^2*((n^2 + 1)/2), {n, 0, 30}]
      => [0, 1, 10, 45, 136, 325, 666, 1225, 2080, 3321, 5050, 7381, 10440, 14365, 19306, 25425, 32896, 41905, 52650, 65341, 80200, 97461, 117370, 140185, 166176, 195625, 228826, 266085, 307720, 354061, 405450]"
  (let [seq [0, 1, 10, 45, 136, 325, 666, 1225, 2080, 3321]  ;; algo. seems to work better with fewer points
                   index (range (count seq))]
               (map #(vec [%1 %2]) index seq)))

